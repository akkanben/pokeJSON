package gson.pokeapi;

public class PokeAPINamedAbility {

	private String name;
	private String url;

	public PokeAPINamedAbility() {
		// empty
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getUrl() {
		return url;
	}

}
